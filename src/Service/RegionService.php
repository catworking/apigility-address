class RegionService
{
    protected $classMethodsHydrator;

    /**
     * @var \Doctrine\ORM\EntityManager
     */
    protected $em;

    public function __construct(ServiceManager $services)
    {
        $this->classMethodsHydrator = new ClassMethodsHydrator();
        $this->em = $services->get('Doctrine\ORM\EntityManager');
    }

    /**
     * 创建一个区域
     *
     * @param $data
     * @return DoctrineEntity\Region
     */
    public function createRegion($data)
    {
        $region = new DoctrineEntity\Region();

        if (isset($data->id)) $region->setId($data->id);
        else $region->setId($this->getLastId());

        $region->setName($data->name);
        $region->setType($data->type);

        if (isset($data->status)) $region->setStatus($data->status);
        else $region->setStatus(DoctrineEntity\Region::STATUS_NONE);

        if (isset($data->region_id)) $region->setParent($this->getRegion($data->region_id));

        $this->em->persist($region);
        $this->em->flush();

        return $region;
    }

    /**
     * 获取一个区域
     *
     * @param $region_id
     * @return DoctrineEntity\Region
     * @throws \Exception
     */
    public function getRegion($region_id)
    {
        $region = $this->em->find('ApigilityAddress\DoctrineEntity\Region', $region_id);
        if (empty($region)) throw new \Exception('区域不存在', 404);
        else return $region;
    }

    /**
     * 获取区域列表
     *
     * @param $params
     * @return DoctrinePaginatorAdapter
     */
    public function getRegions($params)
    {
        $qb = new QueryBuilder($this->em);
        $qb->select('r')->from('ApigilityAddress\DoctrineEntity\Region', 'r');

        $where = '';

        if (isset($params->type)) {
            if (!empty($where)) $where .= ' AND ';
            $where .= 'r.type = :type';
        }

        if (isset($params->region_id)) {
            $qb->innerJoin('r.parent', 'rp');
            if (!empty($where)) $where .= ' AND ';
            $where .= 'rp.id = :region_id';
        }

        if (!empty($where)) {
            $qb->where($where);
            if (isset($params->type)) $qb->setParameter('type', $params->type);
            if (isset($params->region_id)) $qb->setParameter('region_id', $params->region_id);
        }

        $doctrine_paginator = new DoctrineToolPaginator($qb->getQuery());
        return new DoctrinePaginatorAdapter($doctrine_paginator);
    }

    /**
     * 修改区域
     *
     * @param $region_id
     * @param $data
     * @return DoctrineEntity\Region
     * @throws \Exception
     */
    public function updateRegion($region_id, $data)
    {
        $region = $this->getRegion($region_id);

        if (isset($data->name)) $region->setName($data->name);
        if (isset($data->type)) $region->setType($data->type);
        if (isset($data->region_id)) $region->setParent($this->getRegion($region_id));
        if (isset($data->status)) $region->setStatus($data->status);
        $this->em->flush();

        return $region;
    }

    /**
     * 删除一个区域
     *
     * @param $region_id
     * @return bool
     * @throws \Exception
     */
    public function deleteRegion($region_id)
    {
        $region = $this->getRegion($region_id);

        $this->em->remove($region);
        $this->em->flush();

        return true;
    }

    public function getLastId()
    {
        $region = $this->em->getRepository('ApigilityAddress\DoctrineEntity\Region')->findOneBy([], ['id'=>'DESC']);

        if (empty($region)) return 1;
        else return ((int)$region->getId()+1);
    }

